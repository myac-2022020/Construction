    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!"".equals(txtNombre.getText()) || !"".equals(txtCorreo.getText()) || !"".equals(txtPass.getText())){
            lg.setNombre(txtNombre.getText());
            lg.setCorreo(txtCorreo.getText());
            lg.setPass(txtPass.getText());
            login.RegistrarUsuario(lg);
            LimpiarCasillas();
            txtNombre.requestFocus();
            JOptionPane.showMessageDialog(null, "Registrado Exitosamente");
        }else{
            LimpiarCasillas();
            txtNombre.requestFocus();
            JOptionPane.showMessageDialog(null, "Ha Ocurrido Un Error, Intentelo nuevamente");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

